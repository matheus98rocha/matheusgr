    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(
                txtNamePac.getText().trim().isEmpty() ||
                txtIadadePac.getText().trim().isEmpty() ||
                txtCpfPac.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(rootPane, "PREENCHA TODOS OS CAMPOS");
        }else
        {
            String msg;
            Paciente paciente = new Paciente();
            paciente.setNome(txtNamePac.getText().trim());
            paciente.setIdade(Integer.parseInt(txtIadadePac.getText().trim()));
            paciente.setCpf(txtCpfPac.getText().trim());
            msg = pacienteDAO.cadastrarPaciente(paciente);
            
            msg = "Paciente cadastrado com sucesso!!!!";
            
            JOptionPane.showMessageDialog(rootPane, msg);
            this.limparCamposForm();
        }
        
    }//GEN-LAST:event_btnSalvarActionPerformed

